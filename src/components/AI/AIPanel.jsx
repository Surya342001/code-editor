function detectCreateFileTask(text) {
  const raw = (text || '').trim();
  const lower = raw.toLowerCase();
  const asksCreate = lower.includes('create') || lower.includes('make') || lower.includes('genraate');
  const asksFile = lower.includes('file');
  const asksRun = lower.includes('run') || lower.includes('execute');
  if (!asksCreate || !asksFile) return null;

  const isFibonacci =
    lower.includes('fibonacci') ||
    lower.includes('fibanocci') ||
    lower.includes('fibanaci') ||
    lower.includes('fabonacci');
  const isBinarySearch =
    lower.includes('binary search') ||
    lower.includes('binarry search') ||
    lower.includes('binarysearch');
  const isKnapsack =
    lower.includes('knapsack') ||
    lower.includes('kanpsack') ||
    lower.includes('knap sack');

  if (!isFibonacci && !isBinarySearch && !isKnapsack) return null;

  const wantsPython = /\bpython\b|\bpy\b/.test(lower);
  const wantsJs =
    lower.includes('javascript') ||
    lower.includes('javaacript') ||
    lower.includes('java script') ||
    /\bjs\b/.test(lower);

  const language = wantsPython ? 'python' : (wantsJs ? 'javascript' : 'javascript');

  const namedMatch = raw.match(/(?:named|called)\s+([a-zA-Z0-9_.-]+)/i);
  let fileName = namedMatch?.[1] || '';
  const algo = isFibonacci ? 'fibonacci' : (isBinarySearch ? 'binary-search' : 'knapsack');
  if (!fileName) fileName = language === 'python' ? `${algo}.py` : `${algo}.js`;
  if (!fileName.includes('.')) fileName += language === 'python' ? '.py' : '.js';

  const runCommand = language === 'python' ? `python ${fileName}` : `node ${fileName}`;
  return {
    type: isFibonacci ? 'fibonacci' : (isBinarySearch ? 'binary-search' : 'knapsack'),
    language,
    fileName,
    runCommand,
    shouldRun: asksRun,
  };
}

function createTemplate(type, language) {
  if (type === 'binary-search') {
    if (language === 'python') {
      return [
        'def binary_search(arr, target):',
        '    low, high = 0, len(arr) - 1',
        '    while low <= high:',
        '        mid = (low + high) // 2',
        '        if arr[mid] == target:',
        '            return mid',
        '        if arr[mid] < target:',
        '            low = mid + 1',
        '        else:',
        '            high = mid - 1',
        '    return -1',
        '',
        'if __name__ == "__main__":',
        '    nums = [2, 4, 7, 11, 19, 25]',
        '    target = 11',
        '    print(f"Index: {binary_search(nums, target)}")',
      ].join('\n');
    }

    return [
      'function binarySearch(arr, target) {',
      '  let low = 0;',
      '  let high = arr.length - 1;',
      '',
      '  while (low <= high) {',
      '    const mid = Math.floor((low + high) / 2);',
      '    if (arr[mid] === target) return mid;',
      '    if (arr[mid] < target) low = mid + 1;',
      '    else high = mid - 1;',
      '  }',
      '',
      '  return -1;',
      '}',
      '',
      'const nums = [2, 4, 7, 11, 19, 25];',
      'const target = 11;',
      'console.log(`Index: ${binarySearch(nums, target)}`);',
    ].join('\n');
  }

  if (type === 'knapsack') {
    if (language === 'python') {
      return [
        'def knapsack01(weights, values, capacity):',
        '    dp = [0] * (capacity + 1)',
        '    for i in range(len(weights)):',
        '        w, v = weights[i], values[i]',
        '        for c in range(capacity, w - 1, -1):',
        '            dp[c] = max(dp[c], dp[c - w] + v)',
        '    return dp[capacity]',
        '',
        'if __name__ == "__main__":',
        '    weights = [2, 3, 4, 5]',
        '    values = [3, 4, 5, 8]',
        '    capacity = 8',
        '    print(knapsack01(weights, values, capacity))',
      ].join('\n');
    }

    return [
      'function knapsack01(weights, values, capacity) {',
      '  const dp = new Array(capacity + 1).fill(0);',
      '  for (let i = 0; i < weights.length; i++) {',
      '    const w = weights[i];',
      '    const v = values[i];',
      '    for (let c = capacity; c >= w; c--) {',
      '      dp[c] = Math.max(dp[c], dp[c - w] + v);',
      '    }',
      '  }',
      '  return dp[capacity];',
      '}',
      '',
      'const weights = [2, 3, 4, 5];',
      'const values = [3, 4, 5, 8];',
      'const capacity = 8;',
      'console.log(knapsack01(weights, values, capacity));',
    ].join('\n');
  }

  if (language === 'python') {
    return [
      'def fibonacci(n):',
      '    if n <= 1:',
      '        return n',
      '    a, b = 0, 1',
      '    for _ in range(2, n + 1):',
      '        a, b = b, a + b',
      '    return b',
      '',
      'if __name__ == "__main__":',
      '    n = 10',
      '    print(f"Fibonacci({n}) = {fibonacci(n)}")',
    ].join('\n');
  }

  return [
    'function fibonacci(n) {',
    '  if (n <= 1) return n;',
    '  let a = 0;',
    '  let b = 1;',
    '  for (let i = 2; i <= n; i++) {',
    '    const next = a + b;',
    '    a = b;',
    '    b = next;',
    '  }',
    '  return b;',
    '}',
    '',
    'const n = 10;',
    'console.log(`Fibonacci(${n}) = ${fibonacci(n)}`);',
  ].join('\n');
}


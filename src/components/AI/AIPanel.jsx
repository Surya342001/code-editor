function parseFileEdits(text) {
  const edits = [];
  const re = /FILE:\s*([^\n]+)\n```[a-zA-Z0-9_-]*\n([\s\S]*?)```/g;
  let m;
  while ((m = re.exec(text)) !== null)
    edits.push({ filePath: m[1].trim(), content: m[2].replace(/\n$/, '') });
  return edits;
}

function isRunRequest(text) {
  return /\b(run|start|launch|execute)\b[\s\S]*\b(project|app|server|this)\b/i.test(text)
    || /^run this project$/i.test(text.trim());
}

function isKnapsackJsRequest(text) {
  const lower = (text || '').toLowerCase();
  const hasKnapsack = lower.includes('knapsack') || lower.includes('kanpsack') || lower.includes('knap sack');
  const hasJs =
    lower.includes('javascript') ||
    lower.includes('javaacript') ||
    lower.includes('java script') ||
    /\bjs\b/.test(lower);
  const wantsCode = lower.includes('code') || lower.includes('teach') || lower.includes('algorithm');
  return hasKnapsack && hasJs && wantsCode;
}


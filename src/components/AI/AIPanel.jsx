// Count changed lines between two content strings
function countDiff(oldContent, newContent) {
  const oldLines = (oldContent || '').split('\n');
  const newLines = (newContent || '').split('\n');
  const oldSet = new Set(oldLines);
  const newSet = new Set(newLines);
  return {
    added:   newLines.filter(l => !oldSet.has(l)).length,
    removed: oldLines.filter(l => !newSet.has(l)).length,
  };
}

function FilesChangedBar({ edits, onUndo, onKeep }) {
  const [expanded, setExpanded] = useState(false);
  const [undoing,  setUndoing]  = useState(false);
  if (!edits || !edits.length) return null;

  const totalAdded   = edits.reduce((s, e) => s + (e.linesAdded   || 0), 0);
  const totalRemoved = edits.reduce((s, e) => s + (e.linesRemoved || 0), 0);

  const handleUndo = async () => {
    setUndoing(true);
    await onUndo();
    setUndoing(false);
  };

  return (
    <div className="mt-3 border border-[#30363d] rounded-lg overflow-hidden text-xs">
      {/* Header row */}
      <div className="flex items-center gap-2 px-3 py-2 bg-[#161b22]">
        <button
          onClick={() => setExpanded(v => !v)}
          className="flex items-center gap-1.5 text-[#8b949e] hover:text-[#e6edf3] transition-colors"
        >
          <span className="text-[9px]">{expanded ? '▼' : '▶'}</span>
          <span className="font-medium text-[#e6edf3]">
            {edits.length} file{edits.length !== 1 ? 's' : ''} changed
          </span>
          {totalAdded   > 0 && <span className="text-[#3fb950] font-bold">+{totalAdded}</span>}
          {totalRemoved > 0 && <span className="text-[#f85149] font-bold">-{totalRemoved}</span>}
        </button>
        <div className="flex-1" />
        <button
          onClick={onKeep}
          className="px-2.5 py-1 bg-[#238636] hover:bg-[#2ea043] text-white rounded font-semibold transition-colors"
        >
          Keep
        </button>
        <button
          onClick={handleUndo}
          disabled={undoing}
          className="px-2.5 py-1 bg-[#21262d] hover:bg-[#30363d] text-[#8b949e] hover:text-[#e6edf3] rounded border border-[#30363d] font-semibold transition-colors disabled:opacity-50"
        >
          {undoing ? '↩…' : 'Undo'}
        </button>
      </div>

      {/* Expanded file list */}
      {expanded && (
        <div className="border-t border-[#30363d] bg-[#0d1117]">
          {edits.map((e, i) => {
            const parts = e.filePath.replace(/\\/g, '/').split('/');
            const name  = parts.pop();
            const dir   = parts.join('/');
            return (
              <div
                key={i}
                className="flex items-center gap-2 px-3 py-1.5 border-b border-[#21262d] last:border-b-0 hover:bg-[#161b22] transition-colors"
              >
                <span className="text-[#7c3aed]">⊕</span>
                <span className="text-[#e6edf3] font-medium">{name}</span>
                {dir && <span className="text-[#484f58] truncate flex-1 text-[10px]">{dir}</span>}
                <span className="text-[#3fb950] ml-auto">+{e.linesAdded   || 0}</span>
                <span className="text-[#f85149] ml-1">-{e.linesRemoved || 0}</span>
              </div>
            );
          })}
        </div>
      )}
    </div>
  );
}

function ChatMessage({ msg, onUndo, onKeep }) {
  const isUser = msg.role === 'user';
  // Strip FILE: code blocks from visible text — they've already been applied
  const visibleContent = (msg.appliedEdits?.length > 0 || msg.kept || msg.undone)
    ? (msg.content || '').replace(/FILE:[ \t]*[^\n]+\n```[\s\S]*?```/g, '').trim()
    : (msg.content || '');

  return (
    <div className={`flex ${isUser ? 'justify-end' : 'justify-start'} mb-3`}>
      <div
        className={`max-w-[92%] rounded-xl px-3 py-2 text-sm break-words leading-relaxed
          ${isUser
            ? 'bg-[#7c3aed] text-white rounded-br-sm'
            : 'bg-[#21262d] text-[#e6edf3] rounded-bl-sm'}`}
      >
        {msg.role === 'assistant' && msg.loading ? (
          <span className="flex items-center gap-1.5">
            <span className="w-1.5 h-1.5 rounded-full bg-[#8b949e] animate-bounce" />
            <span className="w-1.5 h-1.5 rounded-full bg-[#8b949e] animate-bounce [animation-delay:0.15s]" />
            <span className="w-1.5 h-1.5 rounded-full bg-[#8b949e] animate-bounce [animation-delay:0.3s]" />
          </span>
        ) : (
          <pre className="whitespace-pre-wrap font-sans text-sm">{visibleContent}</pre>
        )}
        {!isUser && <SearchResults results={msg.searchResults} />}
        {!isUser && msg.appliedEdits?.length > 0 && (
          <FilesChangedBar edits={msg.appliedEdits} onUndo={onUndo} onKeep={onKeep} />
        )}
        {!isUser && msg.undone && (
          <div className="mt-2 text-[11px] text-[#8b949e] italic">↩ Changes reverted</div>
        )}
        {!isUser && msg.kept && (
          <div className="mt-2 text-[11px] text-[#3fb950] italic">✓ Changes kept</div>
        )}
      </div>
    </div>
  );
}

